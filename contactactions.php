function contactactions_civicrm_summaryActions(&$actions, $contactID) {
  $formattedAddress = '';

  // lookup the primary address
  $params = array(
    'contact_id' => $contactID,
    'is_primary' => 1,
    'sequential' => 1,
  );
  $addresses = civicrm_api3('Address', 'get', $params);

  if ($addresses['count'] > 0) {
    $formattedAddress = $addresses['values'][0]['street_address'];
    $formattedAddress .= $addresses['values'][0]['supplemental_address_1'];
  }

  $actions['printAddressLabel'] = array(
    'title' => 'Print adreslabel',
    'weight' => 999,
    'ref' => 'print-address-label',
    'key' => 'printAddressLabel',
    'href' => 'http://dit-is-een-test.be?addr=' . $formattedAddress,
  );
}

